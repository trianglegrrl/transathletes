<h1>Growing up in a gender</h1>

<p>Another common criticism of trans women competing in women's sport is that they were raised as boys, with all of the benefits that entails in sport.</p>

<p>This does not take into account that:</p>
<ol>
	<li> Not everyone identified as a boy at a young age is encouraged to do sport.</li>
	<li> Not everyone identified as a boy at a young age enjoys sport.</li>
	<li> Many children identified as girls are encouraged to participate in sport.</li>
</ol>

<p>It is a well-known fact of athletic performance that regular practice improves outcomes. Children are little learning machines, with an incredible ability to acquire new skills. The longer you practice something, the better you become, until you approach the limits of your capabilities.</p>

<p>Competitive sport is different from recreational sport. In recreational sport, winning and losing is valued less than participation, and excellence is much less important. Athletes who excel at competitive sport are not "normal". They are exceptional in some way, whether it is the time they have spent in practice, the social support they receive from their family and peers, or some innate mental or physical attribute that improves their performance.</p>

<p>Transsexuals often experience a disconnect with their bodies, even a profound discomfort in their own skin. Many transsexual women who are athletes didn't adopt sport until adulthood, or even after transition. In this case, a non-trans woman who has been training in a sport since childhood has a distinct advantage in length of time training.</p>

<p>Given that, in elite sports that are segregated by gender, should we subdivide the groups between athletes who started at a young age and athletes who started in adulthood? For example, would you suggest that the girls in the videos below be forced to compete with men when they become adults? Is it unfair for them to compete against other women who might not have been supported in their sport as children or young adults? Of course not; that is one of the things that makes elite athletes elite. </p>

<p>We should support all children who enjoy sport.</p>

