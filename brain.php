<h1>The brain and who we are</h1>

<p class="faq-question">Q: A trans woman went through development as a boy. A brain that is 
more like a boy's than a girl's gives them an advantage in sport.</p>

<p>Okay, three issues here.</p>

<p>First, we need to establish that having a brain that is more like the average boy's than it is like
the average girl's gives you an advantage in sport. </p>

<p>Second, we need to see if a "boy's brain" and a "girl's brain" are made before birth, during
childhood, during puberty, or during adulthood, and if it can change.</p>

<p>Third, we need to establish that there is no overlap between the "girl's brain" and the "boy's
brain" in terms of athletic performance.</p>

<p>We all have different gut feelings about what makes us who we are. One thing is for sure:
our brains are pretty involved in the process.</p>

<p>Our brains develop like any other part of our body, and like any other part of our body are 
made of cells. One of the cool things about the brain is how this mass of cells builds connections
between the cells, and uses this to perform all of the amazing tasks--from breathing to 
appreciating poetry--that it does for us. </p>

<p>The connections formed in our brain are in part based on our experiences (for example, learning []), 
and in part based on what genes were turned on and what chemicals were present when the brain
was going through any of its stages of development. 

Our brain is divided into different regions
that serve different (and often multiple) functions, and how those regions talk to each other
is one of the things that makes us who we are. On an even smaller scale, many of the cells that make up
your brain are constantly strengthening and weakening the connections between themselves.</p>

